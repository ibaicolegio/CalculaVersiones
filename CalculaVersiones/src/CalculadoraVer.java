
public class CalculadoraVer {

	private int n1, n2;

	public CalculadoraVer(int n1, int n2) {
		super();
		this.n1 = n1;
		this.n2 = n2;
	}
	
	public int suma(){
		return n1+n2;
	}
	
	public int resta(){
		return n1-n2;
	}
	
	public int multi(){
		return n1*n2;
	}
	
	public int divide(){
		return n1/n2;
	}
	
	
}
